BEGIN;

CREATE TABLE IF NOT EXISTS regions (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  name_cn TEXT,
  code TEXT,
  country TEXT,
  parent_id BIGINT REFERENCES regions(id) ON DELETE SET NULL,
  description TEXT,
  image_url TEXT,
  bounds JSONB,
  slug TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS locations (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  name_cn TEXT,
  category TEXT DEFAULT 'attraction',
  address TEXT,
  latitude DOUBLE PRECISION NOT NULL,
  longitude DOUBLE PRECISION NOT NULL,
  region_id BIGINT REFERENCES regions(id) ON DELETE SET NULL,
  video_url TEXT,
  facebook_video_url TEXT,
  image_url TEXT,
  images TEXT[] DEFAULT '{}',
  description TEXT,
  review TEXT,
  tags TEXT[] DEFAULT '{}',
  visit_date DATE,
  opening_hours JSONB,
  price_info JSONB,
  google_maps_url TEXT,
  status TEXT DEFAULT 'active',
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_locations_region_id ON locations(region_id);
CREATE INDEX IF NOT EXISTS idx_locations_category ON locations(category);
CREATE INDEX IF NOT EXISTS idx_locations_status ON locations(status);
CREATE INDEX IF NOT EXISTS idx_regions_parent_id ON regions(parent_id);
CREATE INDEX IF NOT EXISTS idx_regions_country ON regions(country);

