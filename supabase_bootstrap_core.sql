CREATE OR REPLACE FUNCTION update_generic_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trigger_update_locations_updated_at ON locations;
CREATE TRIGGER trigger_update_locations_updated_at
BEFORE UPDATE ON locations
FOR EACH ROW
EXECUTE FUNCTION update_generic_updated_at();

DROP TRIGGER IF EXISTS trigger_update_regions_updated_at ON regions;
CREATE TRIGGER trigger_update_regions_updated_at
BEFORE UPDATE ON regions
FOR EACH ROW
EXECUTE FUNCTION update_generic_updated_at();

ALTER TABLE locations ENABLE ROW LEVEL SECURITY;
ALTER TABLE regions ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Enable read access for all users" ON locations;
DROP POLICY IF EXISTS "Enable insert access for all users" ON locations;
DROP POLICY IF EXISTS "Enable update access for all users" ON locations;
DROP POLICY IF EXISTS "Enable delete access for all users" ON locations;

CREATE POLICY "Enable read access for all users"
ON locations FOR SELECT
USING (true);

CREATE POLICY "Enable insert access for all users"
ON locations FOR INSERT
WITH CHECK (true);

CREATE POLICY "Enable update access for all users"
ON locations FOR UPDATE
USING (true);

CREATE POLICY "Enable delete access for all users"
ON locations FOR DELETE
USING (true);

DROP POLICY IF EXISTS "Public regions are viewable by everyone" ON regions;
DROP POLICY IF EXISTS "Enable insert for authenticated users only" ON regions;
DROP POLICY IF EXISTS "Enable update for authenticated users only" ON regions;
DROP POLICY IF EXISTS "Enable delete for authenticated users only" ON regions;

